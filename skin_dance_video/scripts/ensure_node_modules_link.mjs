import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, '..');
const linkPath = path.join(projectRoot, 'node_modules');
const frontendNodeModules = path.resolve(projectRoot, '../../entropydrop_frontend/node_modules');

if (!fs.existsSync(frontendNodeModules)) {
  throw new Error(`Missing frontend node_modules: ${frontendNodeModules}`);
}

if (fs.existsSync(linkPath)) {
  const stat = fs.lstatSync(linkPath);
  if (!stat.isSymbolicLink()) {
    throw new Error(`${linkPath} exists and is not a symlink`);
  }
  const currentTarget = fs.readlinkSync(linkPath);
  const resolved = path.resolve(projectRoot, currentTarget);
  if (resolved !== frontendNodeModules) {
    fs.unlinkSync(linkPath);
    fs.symlinkSync(frontendNodeModules, linkPath, 'dir');
  }
} else {
  fs.symlinkSync(frontendNodeModules, linkPath, 'dir');
}
